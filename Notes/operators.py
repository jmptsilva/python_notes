## Number methods

round(number, ndigits) # rounds a floating number to a specified decimal point
x = 24.16
round(x, 1)
print(x) # expected output = 24.1

pow(base, exp) # raise a number to a specified power
z = pow(4, 2)
print(z) # expected output = 16

abs() # gives the absolute value of a number
v = abs(-3)
print(v)